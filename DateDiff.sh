#!/bin/bash
#//////////////////////////////////////////////////
#//name: DateDiff.sh
#//description: ２つの日付の差分を返す
#//input:
#// 1 --> STARTDAY
#// 2 --> ENDDAY 
#//usage: ex.  ./DateDiff.sh 20181011 20181012
#//////////////////////////////////////////////////
# 開始日の1900/1/1からの秒数を計算
START=`date -d "$1" +%s`
# 終了日の1900/1/1からの秒数を計算
END=`date -d "$2" +%s`
# 終了日 - 開始日の秒数差を計算
DIFF=`expr $END - $START`
# 秒分時でわることで日数が求められる(小数点切り捨て)
DAYS=`expr $DIFF / 60 / 60 / 24`

# 結果出力
echo $DAYS